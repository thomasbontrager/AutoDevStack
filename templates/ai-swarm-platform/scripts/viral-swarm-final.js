#!/usr/bin/env node

/**
 * Viral Swarm Engine
 *
 * Core swarm orchestration engine that:
 * - Spawns multiple AI agents
 * - Reads and optimizes configs
 * - Generates repository ideas
 * - Creates GitHub repos via Octokit
 * - Opens pull requests across repos
 */

import { Octokit } from '@octokit/rest';
import OpenAI from 'openai';
import fs from 'fs/promises';
import path from 'path';
import { fileURLToPath } from 'url';
import dotenv from 'dotenv';

dotenv.config();

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const CONFIG_DIR = path.join(__dirname, '..', 'config');
const HISTORY_DIR = path.join(CONFIG_DIR, 'history');

const octokit = new Octokit({
  auth: process.env.GH_TOKEN,
});

const openai = new OpenAI({
  apiKey: process.env.OPENAI_API_KEY,
});

class SwarmAgent {
  constructor(id, type, config) {
    this.id = id;
    this.type = type;
    this.config = config;
    this.tasksCompleted = 0;
    this.reposCreated = [];
    this.prsOpened = [];
  }

  async generateRepoIdea() {
    console.log(`🤖 Agent ${this.id} generating repository idea...`);

    const prompt = `Generate a unique developer tool or library idea. Return JSON with:
    - name: repository name (lowercase-with-dashes)
    - description: one-line description
    - topics: array of 3-5 relevant tags
    - initial_files: array of {name, content} for initial commit`;

    try {
      const response = await openai.chat.completions.create({
        model: 'gpt-4',
        messages: [
          {
            role: 'system',
            content: 'You are a creative software architect. Generate innovative developer tool ideas.',
          },
          {
            role: 'user',
            content: prompt,
          },
        ],
        temperature: 0.9,
        max_tokens: 800,
      });

      let content = response.choices[0].message.content.trim();

      // Extract JSON
      if (content.includes('```json')) {
        content = content.split('```json')[1].split('```')[0].trim();
      } else if (content.includes('```')) {
        content = content.split('```')[1].split('```')[0].trim();
      }

      return JSON.parse(content);
    } catch (error) {
      console.error(`❌ Agent ${this.id} failed to generate idea:`, error.message);

      // Fallback
      return {
        name: `swarm-tool-${this.id}-${Date.now()}`,
        description: 'AI-generated developer tool',
        topics: ['ai', 'automation', 'devtools'],
        initial_files: [
          {
            name: 'README.md',
            content: '# AI-Generated Tool\n\nGenerated by AI Swarm Platform',
          },
        ],
      };
    }
  }

  async createRepo(owner, idea) {
    console.log(`📦 Agent ${this.id} creating repository: ${idea.name}`);

    try {
      const repo = await octokit.repos.createForAuthenticatedUser({
        name: idea.name,
        description: idea.description,
        auto_init: true,
        private: false,
      });

      console.log(`✅ Repository created: ${repo.data.html_url}`);

      // Add topics
      if (idea.topics && idea.topics.length > 0) {
        await octokit.repos.replaceAllTopics({
          owner,
          repo: idea.name,
          names: idea.topics,
        });
      }

      this.reposCreated.push({
        name: idea.name,
        url: repo.data.html_url,
        created_at: new Date().toISOString(),
      });

      return repo.data;
    } catch (error) {
      console.error(`❌ Agent ${this.id} failed to create repo:`, error.message);
      return null;
    }
  }

  async createInitialFiles(owner, repoName, files) {
    console.log(`📝 Agent ${this.id} adding initial files to ${repoName}...`);

    try {
      for (const file of files) {
        const content = Buffer.from(file.content).toString('base64');

        await octokit.repos.createOrUpdateFileContents({
          owner,
          repo: repoName,
          path: file.name,
          message: `Add ${file.name}`,
          content,
        });
      }

      console.log(`✅ Initial files added`);
    } catch (error) {
      console.error(`❌ Failed to add files:`, error.message);
    }
  }

  async openPullRequest(owner, repo, branch, title, body) {
    console.log(`🔀 Agent ${this.id} opening PR in ${repo}...`);

    try {
      // Create a branch
      const ref = await octokit.git.getRef({
        owner,
        repo,
        ref: 'heads/main',
      });

      await octokit.git.createRef({
        owner,
        repo,
        ref: `refs/heads/${branch}`,
        sha: ref.data.object.sha,
      });

      // Create PR
      const pr = await octokit.pulls.create({
        owner,
        repo,
        title,
        body,
        head: branch,
        base: 'main',
      });

      console.log(`✅ PR opened: ${pr.data.html_url}`);

      this.prsOpened.push({
        repo,
        url: pr.data.html_url,
        created_at: new Date().toISOString(),
      });

      return pr.data;
    } catch (error) {
      console.error(`❌ Failed to open PR:`, error.message);
      return null;
    }
  }

  getMetrics() {
    return {
      id: this.id,
      type: this.type,
      tasksCompleted: this.tasksCompleted,
      reposCreated: this.reposCreated.length,
      prsOpened: this.prsOpened.length,
      repos: this.reposCreated,
      prs: this.prsOpened,
    };
  }
}

class SwarmCoordinator {
  constructor(config) {
    this.config = config;
    this.agents = [];
    this.viralRepos = [];
  }

  async loadConfig() {
    console.log('📖 Loading swarm configuration...');

    try {
      const configPath = path.join(CONFIG_DIR, 'prod.json');
      const configData = await fs.readFile(configPath, 'utf-8');
      this.config = JSON.parse(configData);
      console.log('✅ Configuration loaded');
    } catch (error) {
      console.log('⚠️  Using default configuration');
      this.config = {
        swarm: {
          max_agents: 3,
          agent_types: ['code_generator', 'pr_manager'],
        },
      };
    }
  }

  async loadHistory() {
    console.log('📚 Loading config history...');

    try {
      const files = await fs.readdir(HISTORY_DIR);
      console.log(`✅ Found ${files.length} historical configs`);
    } catch (error) {
      console.log('⚠️  No history found');
    }
  }

  spawnAgents() {
    console.log('🚀 Spawning swarm agents...');

    const maxAgents = this.config.swarm?.max_agents || 3;
    const agentTypes = this.config.swarm?.agent_types || ['code_generator'];

    for (let i = 0; i < maxAgents; i++) {
      const type = agentTypes[i % agentTypes.length];
      const agent = new SwarmAgent(`agent-${i + 1}`, type, this.config);
      this.agents.push(agent);
      console.log(`✅ Spawned ${agent.id} (${type})`);
    }
  }

  async executeSwarmTasks() {
    console.log('\n🎯 Executing swarm tasks...\n');

    const owner = process.env.GH_USERNAME || 'ai-swarm';

    for (const agent of this.agents) {
      console.log(`\n--- Agent ${agent.id} Starting ---`);

      // Generate idea
      const idea = await agent.generateRepoIdea();
      console.log(`💡 Idea: ${idea.name}`);

      // Create repo
      if (this.config.repository?.auto_create !== false) {
        const repo = await agent.createRepo(owner, idea);

        if (repo && idea.initial_files) {
          await agent.createInitialFiles(owner, repo.name, idea.initial_files);

          this.viralRepos.push({
            name: repo.name,
            url: repo.html_url,
            agent: agent.id,
            created_at: new Date().toISOString(),
          });
        }
      }

      agent.tasksCompleted++;
      console.log(`--- Agent ${agent.id} Complete ---\n`);

      // Rate limiting
      await new Promise(resolve => setTimeout(resolve, 2000));
    }
  }

  async saveViralRepos() {
    console.log('💾 Saving viral repos data...');

    try {
      const viralPath = path.join(CONFIG_DIR, 'viral-repos.json');
      await fs.writeFile(viralPath, JSON.stringify(this.viralRepos, null, 2));
      console.log(`✅ Saved to ${viralPath}`);
    } catch (error) {
      console.error('❌ Failed to save viral repos:', error.message);
    }
  }

  async saveAnalytics() {
    console.log('📊 Saving analytics...');

    const analytics = {
      timestamp: new Date().toISOString(),
      totalAgents: this.agents.length,
      totalRepos: this.viralRepos.length,
      agents: this.agents.map(a => a.getMetrics()),
      viralRepos: this.viralRepos,
    };

    try {
      const analyticsPath = path.join(CONFIG_DIR, 'analytics.json');
      await fs.writeFile(analyticsPath, JSON.stringify(analytics, null, 2));
      console.log(`✅ Analytics saved to ${analyticsPath}`);
    } catch (error) {
      console.error('❌ Failed to save analytics:', error.message);
    }
  }

  printSummary() {
    console.log('\n' + '='.repeat(60));
    console.log('🎉 SWARM EXECUTION COMPLETE');
    console.log('='.repeat(60));
    console.log(`Total Agents: ${this.agents.length}`);
    console.log(`Repositories Created: ${this.viralRepos.length}`);
    console.log('\nAgent Metrics:');
    this.agents.forEach(agent => {
      const metrics = agent.getMetrics();
      console.log(`  ${metrics.id}: ${metrics.reposCreated} repos, ${metrics.prsOpened} PRs`);
    });
    console.log('='.repeat(60) + '\n');
  }
}

async function main() {
  console.log('\n' + '='.repeat(60));
  console.log('🌊 AI SWARM PLATFORM - VIRAL SWARM ENGINE');
  console.log('='.repeat(60) + '\n');

  if (!process.env.GH_TOKEN) {
    console.error('❌ Error: GH_TOKEN environment variable is required');
    process.exit(1);
  }

  if (!process.env.OPENAI_API_KEY) {
    console.error('❌ Error: OPENAI_API_KEY environment variable is required');
    process.exit(1);
  }

  const coordinator = new SwarmCoordinator();

  await coordinator.loadConfig();
  await coordinator.loadHistory();
  coordinator.spawnAgents();
  await coordinator.executeSwarmTasks();
  await coordinator.saveViralRepos();
  await coordinator.saveAnalytics();
  coordinator.printSummary();

  console.log('✨ Swarm execution complete!\n');
}

main().catch(error => {
  console.error('💥 Swarm execution failed:', error);
  process.exit(1);
});
